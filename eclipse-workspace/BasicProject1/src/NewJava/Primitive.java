package NewJava;

public class Primitive {

	public static void main(String[] args) {
		// primitive full example
		// integer type
		byte ByteValue=10;//1byte
		short ShortValue=100;//2byte
		int intValue=1000;//2byte
		long LongValue=100000l;//8byte
		
		//Floating point
		float floatValue=10.232f;//4byte
		double doubleVal=26.68;
		
		//Single Char
		char charVal='1';
		boolean booleanVal=true;
		
		System.out.println("byte:"+ByteValue);
		System.out.println("short:"+ShortValue);
		System.out.println("int:"+intValue);
		System.out.println("long:"+LongValue);
		System.out.println("float:"+floatValue);
		System.out.println("double:"+doubleVal);
		System.out.println("char:"+charVal);
		System.out.println("boolean:"+booleanVal);
	}
	}


